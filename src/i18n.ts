const messages: Record<Lang, Dict> = { en, ru };

function detectLang(): Lang {
  if (typeof navigator !== "undefined") {
    const sys = (navigator.language || "").toLowerCase();
    if (sys.startsWith("ru")) return "ru";
  }
  return "en";
}

function detectTheme(): Theme {
  if (typeof window !== "undefined" && window.matchMedia) {
    return window.matchMedia("(prefers-color-scheme: dark)").matches ? "dark" : "light";
  }
  return "light";
}

/**
 * App-level preferences (language, theme). These are viewer settings, not part of
 * the project document — so they live outside the project store and outside undo/redo.
 */
interface AppPrefsState {
  lang: Lang;
  setLang: (lang: Lang) => void;
  theme: Theme;
  setTheme: (theme: Theme) => void;
  /** Whether the first-run welcome flow has been completed/dismissed. */
  onboarded: boolean;
  setOnboarded: (v: boolean) => void;
}

export const useI18n = create<AppPrefsState>()(
  persist(
    (set) => ({
      lang: detectLang(),
      setLang: (lang) => set({ lang }),
      theme: detectTheme(),
      setTheme: (theme) => set({ theme }),
      onboarded: false,
      setOnboarded: (onboarded) => set({ onboarded }),
    }),
    { name: "seating-planner:lang" },
  ),
);

export function translate(lang: Lang, key: string): string {
  return messages[lang][key] ?? messages.en[key] ?? key;
}

/** Hook returning a translate function bound to the current language. */
export function useT() {
  const lang = useI18n((s) => s.lang);
  return (key: string) => translate(lang, key);
}
